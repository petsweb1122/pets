    <button class="faq8" data-toggle="collapse" data-target="#faq8"><i class="fa fa-check" aria-hidden="true"></i> Protect
      pets if you are sick?</button>
    <div id="faq8" class="collapse">
      <p>If you are sick with COVID-19 (either suspected or confirmed by a test), you should restrict contact with your
        pets and other animals, just like you would with people. Until we know more about this virus, people sick with
        COVID-19 should <strong>avoid contact with pets and other animals</strong>.</p>
      <ul>
        <li>When possible, have another member of your household care for your pets while you are sick.</li>
        <li>Avoid contact with your pet including petting, snuggling, being kissed or licked, sharing food, and sleeping
          in the same bed.</li>
        <li>If you must care for your pet or be around animals while you are sick, wear a cloth face covering and wash
          your hands before and after you interact with them.</li>
      </ul>
      <p>If you are sick with COVID-19 and your pet becomes sick, <strong>do not take your pet to the veterinary clinic
          yourself</strong>. Call your veterinarian and let them know you have been sick with COVID-19. Some veterinarians
        may offer telemedicine consultations or other plans for seeing sick pets. Your veterinarian can evaluate your pet
        and determine the next steps for your pet’s treatment and care.</p>
      <p>For more information visit: <a title=">What to Do if You are Sick" href="https://www.cdc.gov/coronavirus/2019-ncov/if-you-are-sick/steps-when-sick.html">What to Do if You are
          Sick.</a></p>
    </div>
  
    <button class="faq9" data-toggle="collapse" data-target="#faq9"><i class="fa fa-check" aria-hidden="true"></i> Stay
      healthy around animals?</button>
    <div id="faq9" class="collapse">
      <p>In the United States, there is no evidence that animals are playing a significant role in the spread of COVID-19.
        Based on the limited information available to date, the risk of animals spreading COVID-19 to people is considered
        to be low. However, because all animals can carry germs that can make people sick, it’s always a good idea to
        practice <a title="Healthy habits" href="https://www.cdc.gov/healthypets/publications/stay-healthy-pets.html">healthy habits</a> around
        pets and other animals.
      </p>
      <ul>
        <li>Wash your hands after handling animals, their food, waste, or supplies.</li>
        <li>Practice good pet hygiene and clean up after pets properly.</li>
        <li>Talk to your veterinarian if you have questions about your pet’s health.</li>
        <li>Be aware that <a title="Children 5 years of age and younger" href="https://www.cdc.gov/healthypets/specific-groups/children.html">children 5 years of age
            and younger</a>, people with weakened immune systems, and older adults are more likely to get sick from germs
          some animals can carry.</li>
      </ul>
      <p>For more information, visit <a title="CDC’s COVID-19 and Animals" href="https://www.cdc.gov/coronavirus/2019-ncov/daily-life-coping/animals.html">CDC’s COVID-19 and Animals</a>
        webpage and <a title="Healthy Pets, Healthy People" href="https://www.cdc.gov/healthypets/">Healthy Pets, Healthy People</a> website.</p>
    </div>
  
