    <h1>PETSSIFIED - COVID19 FREQUENTLY ASKED QUESTIONS (COVID19 FAQS)</h1>
    <hr>
    <button class="faq1" data-toggle="collapse" data-target="#faq1"><i class="fa fa-check" aria-hidden="true"></i> What
      you need to know?</button>
    <div id="faq1" class="collapse">
      <ul>
        <li>A small number of pets worldwide, including cats and dogs, have been reported to be infected with the virus
          that causes COVID-19, mostly after close contact with people with COVID-19.</li>
        <li>Based on the limited information available to date, the risk of animals spreading COVID-19 to people is
          considered to be low.</li>
        <li>It appears that the virus that causes COVID-19 can spread from people to animals in some situations.</li>
        <li>Treat pets as you would other human family members – do not let pets interact with people outside the
          household.</li>
        <li>If a person inside the household becomes sick, isolate that person from everyone else, including pets.</li>
        <li>This is a rapidly evolving situation and information will be updated as it becomes available.</li>
      </ul>
    </div>
  
    <button class="faq2" data-toggle="collapse" data-target="#faq2"><i class="fa fa-check" aria-hidden="true"></i> What is
      the connection between COVID-19 and animals?</button>
    <div id="faq2" class="collapse">
      <p>COVID-19 is spread through human-to-human transmission.</p>
      <p>We already know a lot about other viruses in the coronavirus family and most of these types of viruses have an
        origin in animals. The COVID-19 virus (also called SARS-CoV-2) is a new virus in humans. The possible animal
        source of COVID-19 has not yet been confirmed but research is ongoing.</p>
      <p>WHO continues to monitor the latest research on this and other COVID-19 topics and will update, as new findings
        are available.</p>
    </div>
  
    <button class="faq3" data-toggle="collapse" data-target="#faq3"><i class="fa fa-check" aria-hidden="true"></i> Can I
      catch COVID-19 from my pet or other animals?</button>
    <div id="faq3" class="collapse">
      <p></p>
      <p>Several dogs and cats (domestic cats and tigers) in contact with infected humans have tested positive for
        COVID-19. In addition, ferrets appear to be susceptible to the infection. In experimental conditions, both cats
        and ferrets were able to transmit infection to other animals of the same species. However, there is no evidence
        that these animals can transmit the disease to humans and spread COVID-19. COVID-19 is mainly spread through
        droplets produced when an infected person coughs, sneezes, or speaks.</p>
      <p>Minks raised in farms have also been detected with the virus. Most likely, they have been infected by farm
        workers. In a few instances, the minks that were infected by humans have transmitted the virus to other people.
        These are the first reported cases of animal-to-human transmission.</p>
      <p>It is still recommended that people who are sick with COVID-19 and people who are at risk limit contact with
        companion and other animals. When handling and caring for animals, basic hygiene measures should always be
        implemented. This includes hand washing after handling animals, their food or supplies, as well as avoiding
        kissing, licking or sharing food.</p>
      <p>More recommendations are available on the OIE website:
        https://www.oie.int/en/scientific-expertise/specific-information-and-recommendations/questions-and-answers-on-2019novel-coronavirus/
      </p>
      <p>WHO continues to monitor the latest research on this and other COVID-19 topics and will update as new findings
        are available.</p>
    </div>
  
    <button class="faq4" data-toggle="collapse" data-target="#faq4"><i class="fa fa-check" aria-hidden="true"></i> How
      long does the virus survive on surfaces?</button>
    <div id="faq4" class="collapse">
      <p>The most important thing to know about coronavirus on surfaces is that they can easily be cleaned with common
        household disinfectants that will kill the virus. Studies have shown that the COVID-19 virus can survive for up to
        72 hours on plastic and stainless steel, less than 4 hours on copper and less than 24 hours on cardboard.</p>
      <p>As, always clean your hands with an alcohol-based hand rub or wash them with soap and water. Avoid touching your
        eyes, mouth, or nose.</p>
    </div>
  
    <button class="faq5" data-toggle="collapse" data-target="#faq5"><i class="fa fa-check" aria-hidden="true"></i> From
      the CDC in the United States of America</button>
    <div id="faq5" class="collapse">
      <p>
        https://www.who.int/emergencies/diseases/novel-coronavirus-2019/question-and-answers-hub/q-a-detail/q-a-coronaviruses?gclid=CjwKCAjwx9_4BRAHEiwApAt0zjAXrgBD0xprkTKDt4BMIiHQ6rxj527ypRmPFEmJE4DLi_BzdZuKVxoCQgkQAvD_BwE#:~:text=pet
      </p>
      <p>Coronaviruses are a large family of viruses. Some coronaviruses cause cold-like illnesses in people, while others
        cause illness in certain types of animals, such as cattle, camels, and bats. Some coronaviruses, such as canine
        and feline coronaviruses, infect only animals and do not infect humans.</p>
    </div>
  
    <button class="faq6" data-toggle="collapse" data-target="#faq6"><i class="fa fa-check" aria-hidden="true"></i> Risk of
      people spreading the virus that causes COVID-19 to pets</button>
    <div id="faq6" class="collapse">
      <p>We are still learning about the virus that causes COVID-19, but it appears that it can spread from people to
        animals in some situations. A small number of pets worldwide, including cats and dogs, have been reported to be
        infected with the virus that causes COVID-19, mostly after close contact with people with COVID-19.</p>
      <p>Infected pets might get sick or they might not have any symptoms. Of the pets that have gotten sick, most only
        had mild illness and fully recovered.</p>
    </div>
  
    <button class="faq7" data-toggle="collapse" data-target="#faq7"><i class="fa fa-check" aria-hidden="true"></i> What to
      do if you own pets?</button>
    <div id="faq7" class="collapse">
      <p>Until we learn more about how this virus affects animals, treat pets as you would other human family members to
        protect them from a possible infection.</p>
      <p>Because there is a small risk that people with COVID-19 could spread the virus to animals, CDC recommends that
        pet owners limit their pet’s interaction with people outside their household.</p>
      <ul>
        <li>Keep cats indoors when possible and do not let them roam freely outside.</li>
        <li>Walk dogs on a leash at least 6 feet (2 meters) away from others.</li>
        <li>Avoid public places where a large number of people gather.</li>
        <li>Do not put face coverings on pets. Covering a pet’s face could harm them.</li>
      </ul>
      <p>There is no evidence that the virus can spread to people from the skin, fur, or hair of pets. Do not wipe or
        bathe your pet with chemical disinfectants, alcohol, hydrogen peroxide, or any other products not approved for
        animal use.</p>
      <p>Talk to your veterinarian if your pet gets sick or if you have any concerns about your pet’s health.</p>
    </div>
